function openProfile(id) {
  const s = staff.find(m => m.id === id);
  if (!s) return;

  const bannerEl  = document.getElementById('modal-banner');
  const bannerImg = document.getElementById('modal-banner-img');
  bannerImg.classList.add('hidden');
  if (s.bannerImg) {
    bannerEl.style.background = `url('${s.bannerImg}') center / cover no-repeat`;
  } else {
    bannerEl.style.background = s.bannerColor;
  }

  const av       = document.getElementById('modal-avatar');
  const avImg    = document.getElementById('modal-avatar-img');
  const avLetter = document.getElementById('modal-avatar-letter');
  if (s.avatarImg) {
    av.style.background = 'transparent';
    avImg.src = s.avatarImg;
    avImg.classList.remove('hidden');
    avLetter.textContent = '';
  } else {
    avImg.classList.add('hidden');
    avImg.src = '';
    av.style.background = s.avatarColor;
    avLetter.textContent = s.emoji;
  }

  document.getElementById('modal-avatar-status').className = `modal-avatar-status ${s.status}`;
  document.getElementById('modal-name').textContent = s.name;
  document.getElementById('modal-tag').textContent  = s.tag;

  const bioEl = document.getElementById('modal-bio');
  bioEl.style.whiteSpace = 'pre-line';
  bioEl.innerHTML = s.bio;

  const cs = document.getElementById('modal-custom-status');
  if (s.customStatus && s.customStatus.text) {
    cs.innerHTML = `<span class="cs-emoji">${s.customStatus.emoji}</span><span class="cs-text">${s.customStatus.text}</span>`;
    cs.style.display = 'flex';
  } else {
    cs.style.display = 'none';
  }

  const rainbowClass = s.id === 6 ? ' rainbow' : '';
  document.getElementById('modal-roles').innerHTML = `
    <span class="role-badge ${s.role}${rainbowClass}"><span class="dot"></span>${s.roleLabel}</span>
    <span class="info-chip"> Since ${s.joined}</span>
    <span class="info-chip status-chip ${s.status}">● ${STATUS_LABELS[s.status] || s.status}</span>`;
  document.getElementById('modal-perms').innerHTML =
    s.perms.map(p => `<span class="perm-tag">${p}</span>`).join('');

  const spotifyEl = document.getElementById('modal-spotify');
  if (spotifyEl) {
    spotifyEl.innerHTML = s.spotify ? buildSpotifyEmbed(s.spotify) : '';
    spotifyEl.style.display = s.spotify ? 'block' : 'none';
  }

  document.getElementById('modal-overlay').classList.add('open');
}

function closeModal() {
  document.getElementById('modal-overlay').classList.remove('open');
}

